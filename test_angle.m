function show_data(path, file1, file2)
len = length(path) - 2;
path_d1(1:len, 1:2) = 0;
for t = 1:len
    i = t + 1;
    if i > len
        i = 1;
    end
    path_d1(t, :) = path(i, :) - path(t, :);
end
path_d2(1:len, 1:2) = 0;
for t = 1:len
    i = t + 1;
    if i > len
        i = 1;
    end
    path_d2(t, :) = path_d1(i, :) - path_d1(t, :);
end
k(1:len) = 0;
for t = 1:len
    k(t) = (path_d1(t, 1) * path_d2(t, 2) - path_d2(t, 1) * path_d1(t, 2)) /...
           (path_d1(t, 1)^2 + path_d1(t, 2)^2)^1.5;
end
k_big(1:len) = 0;
for t = 1:len
    if t > 1
        k_big(t) = k_big(t - 1);
    end
    k_big(t) = k_big(t) + abs(k(t));
end
axis = figure;
plot(path(:, 1), path(:, 2))
saveas(axis, file1)
axis = figure;
subplot(1, 2, 1)
plot(k)
title('Curvature')
subplot(1, 2, 2)
plot(k_big)
title('K')
saveas(axis, file2)
end
